    /**
     * Update the specified resource in storage.
     */
    public function update(Request $request, User $user)
    {
        $user->administration_level = $request->administration_level;
        $user->save();

        $administration_name = '';
        switch($request->administration_level){
                case $this->users->superAdmin():
                    $administration_name = 'مدير عام';
                break;
                case $this->users->admin():
                    $administration_name = 'مدير';
                break;
                default:
                    $administration_name = 'مستخدم';
        }
        
        return Response::json([
            'administration_level' =>  $request->administration_level,
            'administration_name' => $administration_name
        ]);
    }

